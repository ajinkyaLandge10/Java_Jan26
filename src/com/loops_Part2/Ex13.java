package com.loops_Part2;

public class Ex13 {

	public static void main(String[] args) {
		// ternary operator

		int n1 = 5, n2 = 10, max;

		System.out.println(n1); // 5
		System.out.println(n2); // 10

		max = (n1 > n2) ? n1 : n2;
		System.out.println("max = " + max); // 10

		// syntax:
		// variable = (condition) ? v1: v2;
	}

}
