package com.loops;

public class Ex15 {

	public static void main(String[] args) {

//		-128 to 127
		
//		-128 to 0 byte
		for(byte i=-128 ;i<=0;i++) {
			System.out.println(i);
		}
		
		System.out.println("----------------------------");
//		1 to 127 int
		for(int i=1 ;i<=127;i++) {
			System.out.println(i);
		}

	}

}
