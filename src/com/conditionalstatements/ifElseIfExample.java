package com.conditionalstatements;

public class ifElseIfExample {

	public static void main(String[] args) {
		int no =0;
		if(no > 0) {
			System.out.println(no+" = "+"The Number is Positive");
		}else if(no <0) {
			System.out.println(no+" = "+"The Number is Negative");
		}else {
			System.out.println(no+" = "+"The Number is Neutral");
		}

	}

}
